    public function getDataAPIRest($number) {
        $guzzleClient = new GuzzleClient();
        
        $res = $guzzleClient->request('GET', 'https://xkcd.com/'.$number.'/info.0.json');
        
        $responseBody = json_decode($res->getBody(), true);
                
        $data['title'] = $responseBody['title'];
        $data['img'] = $responseBody['img'];
                
        return response()->json($data);
    }

